function city_country(city :string, country : string) : string{
    return`${city} ,${country}`;
}
//calling function and printing the returned value
 console.log(city_country("Makkah","Saudia Arabia"));
 console.log(city_country("Baghdad","Iraq"));
 console.log(city_country("Istambul","Turkey"));